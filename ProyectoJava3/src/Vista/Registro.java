    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        ConUsr ob = new ConUsr();
        RUsuario ob2 = new RUsuario();
        
        String pass = new String(TxtPassword.getPassword());
        String passconf = new String(TxtPassConfir.getPassword());
        
        if(pass.equals(passconf)){
            String nuevoPass = hash.sha1(pass);
            
            ob2.setUsuario(TxtUsuario.getText());
            ob2.setPassword(nuevoPass);
            ob2.setNombre(TxtNombre.getText());
            ob2.setCorreo(TxtCorreo.getText());
            ob2.setId_tip_usr(1);
            
            if(ob.registrar(ob2)){
                JOptionPane.showMessageDialog(null,"Se ha registrado el usuario");
            }else{
                JOptionPane.showMessageDialog(null,"Error al almacenar");
            }
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Las contrase√±as no coinciden");
        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

